int		ft_atoi(char *str)
{
	int i;
	int res;
	int concat;

	i = 0;
	sig = 1;
	res = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			res *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9') //i = 1  / 2
	{
		concat = (str[i] - '0') + (res * 10); //1 +10 = 11 /12 / 2
		i++;
	}
	return (res * sig);
}

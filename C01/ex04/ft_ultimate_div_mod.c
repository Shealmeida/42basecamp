void	ft_ultimate_div_mod(int *a, int *b)
{
	int c;
	int d;

	if (b != 0)
	{
		c = *a;
		d = *b;
		*a = c / d;
		*b = c % d;
	}
	else
	{
		*a = '\0';
		*b = '\0';
	}
}
